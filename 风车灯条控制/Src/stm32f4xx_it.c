void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	
	switch(GPIO_Pin)   /////需加一个on_off判断语句
	{
		case GPIO_PIN_0 :
		{
			if(LED1.on_off)
			LED1.state = 1 ;
		}
		break;
		
		case GPIO_PIN_4 :
		{
			if(LED2.on_off)
			LED2.state = 1 ;
		}
		break;

		case GPIO_PIN_12 :
		{
			if(LED3.on_off)
			LED3.state = 1;
		}
		break;

		case GPIO_PIN_9 :
		{
			if(LED4.on_off)
			LED4.state = 1;
		}
		break;

		case GPIO_PIN_1 :
		{
			if(LED5.on_off)
			LED5.state = 1 ;
		}
		break;		
		
	}
}
